    private void BfinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BfinalizarActionPerformed
        // TODO add your handling code here:
        boolean valid=false;
        int pos=0;
        if(TFidtrabajo.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe ingresar una ID", "ERROR", JOptionPane.WARNING_MESSAGE);
        }else{
            //buscamos el ID
            for (int i = 0; i < registro.trabajos.size(); i++) {
                if(registro.trabajos.get(i).getEstado()==false){
                    if(registro.trabajos.get(i).getId().equalsIgnoreCase(TFidtrabajo.getText())){
                        valid=true;
                        pos=i;
                    }
                }
            }
            
            //validamos si lo encontrÃ³
            if(valid){
                int vc=0;
                registro.trabajos.get(pos).changeEstado();
                JOptionPane.showMessageDialog(this, "Estado cambiado con exito a trabajo con ID: "+registro.trabajos.get(pos).getId());
                
                //Actualizamos la tabla
                DefaultTableModel modelo = (DefaultTableModel) Ttrabajosactivos.getModel();
                modelo.getDataVector().clear();
                //creamos filas en la table si hay trabajos en proceso
                for (int k = 0; k < registro.trabajos.size(); k++) {
                    if(registro.trabajos.get(k).getEstado()==false){
                        modelo.addRow(new Object[]{"", "", "", "", ""});
                    }
                }
                for (int i = 0; i < registro.trabajos.size(); i++) {
                    if(registro.trabajos.get(i).getEstado()==false){
                        System.out.println(vc);
                        Ttrabajosactivos.setValueAt(registro.trabajos.get(i).getId(), vc, 0);
                        Ttrabajosactivos.setValueAt(registro.trabajos.get(i).getTipotrabajo(), vc, 1);
                        Ttrabajosactivos.setValueAt(registro.trabajos.get(i).getDescripcion(), vc, 2);
                        Ttrabajosactivos.setValueAt(registro.trabajos.get(i).getNhoras(), vc, 3);
                        if(registro.trabajos.get(i).getEstado()==false){
                            Ttrabajosactivos.setValueAt("En proceso", vc, 4);
                        }else{
                            Ttrabajosactivos.setValueAt("Finalizado", vc, 4);
                        }
                        vc++;
                    }
                }
                    
            }else{
                JOptionPane.showMessageDialog(this, "ID no encontrada");
            }
        }

        

    }//GEN-LAST:event_BfinalizarActionPerformed

