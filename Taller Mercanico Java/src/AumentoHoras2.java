    private void BaddhoraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BaddhoraActionPerformed
        // TODO add your handling code here:
        boolean valid=false;
        int pos=0;

        //buscamos el id
        if(TFidtrabajo.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe ingresar una ID", "ERROR", JOptionPane.WARNING_MESSAGE);
        }else{
            for (int i = 0; i < registro.trabajos.size(); i++) {
                if(registro.trabajos.get(i).getEstado()==false){
                    if(registro.trabajos.get(i).getId().equalsIgnoreCase(TFidtrabajo.getText())){
                        valid=true;
                        pos=i;
                    }
                }
            }

            //validamos si lo encontrÃ³
            if(valid){
                if(registro.trabajos.get(pos).getEstado()==false){
                    //agragamos hora
                    registro.trabajos.get(pos).setNhoras();
                    JOptionPane.showMessageDialog(this, "Hora agregada con exito a trabajo con ID: "+registro.trabajos.get(pos).getId());
                }else{
                    JOptionPane.showMessageDialog(this, "Este trabajo ya estÃ¡ finalizado");
                }

                //Actualizamos la tabla
                DefaultTableModel modelo = (DefaultTableModel) Thoras.getModel();
                modelo.getDataVector().clear();
                //creamos filas en la table si hay trabajos en proceso
                for (int k = 0; k < registro.trabajos.size(); k++) {
                    if(registro.trabajos.get(k).getEstado()==false){
                        modelo.addRow(new Object[]{"", "", "", "", ""});
                    }
                }
                vc=0;
                for (int i = 0; i < registro.trabajos.size(); i++) {
                    if(registro.trabajos.get(i).getEstado()==false){
                        Thoras.setValueAt(registro.trabajos.get(i).getId(), vc, 0);
                        Thoras.setValueAt(registro.trabajos.get(i).getTipotrabajo(), vc, 1);
                        Thoras.setValueAt(registro.trabajos.get(i).getDescripcion(), vc, 2);
                        Thoras.setValueAt(registro.trabajos.get(i).getNhoras(), vc, 3);
                        if(registro.trabajos.get(i).getEstado()==false){
                            Thoras.setValueAt("En proceso", vc, 4);
                        }else{
                            Thoras.setValueAt("Finalizado", vc, 4);
                        }
                        vc++;
                    }
                }

            }else{
                JOptionPane.showMessageDialog(this, "ID no encontrada");
            }
        }

    }//GEN-LAST:event_BaddhoraActionPerformed

